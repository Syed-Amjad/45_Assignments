// Name Cases: Store a person’s name in a variable,
// and then print that person’s name in lowercase, uppercase, and titlecase.
var fulName = "syed amjad ali";
console.log(fulName.toLowerCase());
console.log(fulName.toUpperCase());
console.log(fulName.charAt(0).toUpperCase() + fulName.slice(1, 4), fulName.charAt(5).toUpperCase() + fulName.slice(6, 10), fulName.charAt(11).toUpperCase() + fulName.slice(12));
